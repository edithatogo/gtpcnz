def scenario_input_frame(scenarios: Sequence[Scenario] = PARAMETERISED_SCENARIOS) -> pd.DataFrame:
    return pd.DataFrame([asdict(s) for s in scenarios])


def run_parameterised_game_models(scenarios: Sequence[Scenario] = PARAMETERISED_SCENARIOS) -> pd.DataFrame:
    rows: list[dict[str, object]] = []
    for scenario in scenarios:
        for game_id, model in GAME_MODELS.items():
            row = model(scenario).as_row()
            rows.append(row)
    return pd.DataFrame(rows)


def run_parameterised_hybrid_model(scenarios: Sequence[Scenario] = PARAMETERISED_SCENARIOS) -> pd.DataFrame:
    rows: list[dict[str, object]] = []
    for scenario in scenarios:
        row = hybrid_outcome(scenario).as_row()
        if row["funding_architecture"] == "unspecified architecture":
            row["funding_architecture"] = scenario.description
        rows.append(row)
    return pd.DataFrame(rows)


def run_parameterised_uncertainty(
    n_per_scenario: int = 1000,
    sd: float = 0.07,
    seed: int = 20260508,
    scenarios: Sequence[Scenario] = PARAMETERISED_SCENARIOS,
) -> tuple[pd.DataFrame, pd.DataFrame]:
    rng = np.random.default_rng(seed)
    rows: list[dict[str, object]] = []
    hybrid_rows: list[dict[str, object]] = []
    for scenario in scenarios:
        for draw in range(n_per_scenario):
            perturbed = perturb_scenario(scenario, rng, sd=sd)
            params = {field: getattr(perturbed, field) for field in PARAMETER_FIELDS}
            h = hybrid_outcome(perturbed).as_row()
            h["draw"] = draw
            h.update(params)
            hybrid_rows.append(h)
            for game_id, model in GAME_MODELS.items():
                g = model(perturbed).as_row()
                g["draw"] = draw
                g.update(params)
                rows.append(g)
    return pd.DataFrame(rows), pd.DataFrame(hybrid_rows)


def summarise_parameterised_uncertainty(hybrid_draws: pd.DataFrame) -> pd.DataFrame:
    metrics = [
        "weighted_access", "weighted_provider_viability", "weighted_equity", "weighted_fiscal_control",
        "weighted_hospital_pressure", "weighted_gaming_risk", "weighted_welfare", "supply_generation_index",
        "equity_legitimacy_index", "governance_resilience_index", "hospital_deflection_index",
        "implementation_readiness_index", "fiscal_gaming_risk_index", "interaction_penalty", "hybrid_viability_score",
    ]
    rows = []
    for (sid, sname), subset in hybrid_draws.groupby(["scenario_id", "scenario_name"], sort=True):
        row = {"scenario_id": sid, "scenario_name": sname, "n_draws": subset["draw"].nunique()}
        for metric in metrics:
            values = subset[metric].astype(float)
            row[f"{metric}_mean"] = values.mean()
            row[f"{metric}_p05"] = values.quantile(0.05)
            row[f"{metric}_p50"] = values.quantile(0.50)
            row[f"{metric}_p95"] = values.quantile(0.95)
        rows.append(row)
    return pd.DataFrame(rows).round(2)


def sensitivity_by_parameter(draws: pd.DataFrame, outcome: str = "hybrid_viability_score", top_n: int = 12) -> pd.DataFrame:
    rows: list[dict[str, object]] = []
    for sid, subset in draws.groupby("scenario_id", sort=True):
        y = subset[outcome].astype(float)
        for param in PARAMETER_FIELDS:
            x = subset[param].astype(float)
            if x.nunique() <= 1 or y.nunique() <= 1:
                corr = np.nan
            else:
                corr = float(np.corrcoef(x, y)[0, 1])
            rows.append({"scenario_id": sid, "outcome": outcome, "parameter": param, "correlation": corr, "abs_correlation": abs(corr) if not np.isnan(corr) else np.nan})
    df = pd.DataFrame(rows).dropna().sort_values(["scenario_id", "abs_correlation"], ascending=[True, False])
    return df.groupby("scenario_id").head(top_n).reset_index(drop=True).round(4)


_MCDA_WEIGHTS = {
    "access_supply": 12, "hospital_deflection": 12, "equity_legitimacy": 14, "rural_inperson": 9,
    "fiscal_sustainability": 12, "gaming_low_value": 10, "admin_market_entry": 8,
    "governance_safety": 10, "political_feasibility": 6, "data_accountability": 7,
}


def hybrid_informed_mcda(scenarios: Sequence[Scenario] = PARAMETERISED_SCENARIOS) -> pd.DataFrame:
    hybrid = run_parameterised_hybrid_model(scenarios)
    scenario_df = scenario_input_frame(scenarios)
    merged = hybrid.merge(scenario_df[["scenario_id", *PARAMETER_FIELDS]], on="scenario_id", how="left")
    rows = []
    total_weight = sum(_MCDA_WEIGHTS.values())
    for _, r in merged.iterrows():
        criteria = {
            "access_supply": 0.55 * float(r["supply_generation_index"]) + 0.45 * float(r["weighted_access"]),
            "hospital_deflection": float(r["hospital_deflection_index"]),
            "equity_legitimacy": float(r["equity_legitimacy_index"]),
            "rural_inperson": 100 * (0.60 * float(r["local_inperson_loading"]) + 0.20 * float(r["telehealth_integration"]) + 0.20 * float(r["ambulance_alternative_funding"])),
            "fiscal_sustainability": 0.55 * float(r["weighted_fiscal_control"]) + 0.45 * (100 - float(r["fiscal_gaming_risk_index"])),
            "gaming_low_value": 100 - float(r["fiscal_gaming_risk_index"]),
            "admin_market_entry": 100 * (0.55 * float(r["direct_claiming"]) + 0.45 * (1 - float(r["pho_transaction_cost"]))),
            "governance_safety": float(r["governance_resilience_index"]),
            "political_feasibility": 100 * (0.45 * float(r["stakeholder_alignment"]) + 0.35 * float(r["narrative_coherence"]) + 0.20 * (1 - max(0.0, float(r["copayment_level"]) - float(r["copayment_protections"])))),
            "data_accountability": 100 * (0.45 * float(r["data_observability"]) + 0.25 * float(r["primary_kpi_salience"]) + 0.20 * float(r["ambulance_kpi_salience"]) + 0.10 * float(r["gaming_controls"])),
        }
        weighted_score = sum(criteria[k] * _MCDA_WEIGHTS[k] for k in _MCDA_WEIGHTS) / total_weight
        risk_penalty = 0.16 * float(r["fiscal_gaming_risk_index"]) + 0.10 * float(r["interaction_penalty"])
        row = {
            "scenario_id": r["scenario_id"],
            "scenario_name": r["scenario_name"],
            "weighted_score_before_penalty": round(weighted_score, 2),
            "risk_penalty": round(risk_penalty, 2),
            "risk_adjusted_mcda_score": round(max(0, min(100, weighted_score - risk_penalty)), 2),
        }
        row.update({k: round(v, 2) for k, v in criteria.items()})
        rows.append(row)
    result = pd.DataFrame(rows)
    result["rank"] = result["risk_adjusted_mcda_score"].rank(ascending=False, method="min").astype(int)
    return result.sort_values(["rank", "risk_adjusted_mcda_score"], ascending=[True, False]).reset_index(drop=True)
