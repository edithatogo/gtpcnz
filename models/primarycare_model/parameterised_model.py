def parameter_input_frame() -> pd.DataFrame:
    return pd.DataFrame([asdict(p) for p in PARAMETER_INPUTS])


_CURRENT = {p.parameter: p.current_normalised_value for p in PARAMETER_INPUTS}


def _scenario(scenario_id: str, name: str, description: str, **updates: float) -> Scenario:
    values = dict(_CURRENT)
    values.update(updates)
    missing = set(PARAMETER_FIELDS) - set(values)
    if missing:
        raise ValueError(f"missing parameter values: {missing}")
    return Scenario(scenario_id=scenario_id, name=name, description=description, **{k: float(values[k]) for k in PARAMETER_FIELDS})


